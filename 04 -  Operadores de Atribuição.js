/*


Operadores de Atribuição são usados para atribuir valores a variáveis. O operador de atribuição mais comum é o sinal de igual (=), 
que atribui o valor do lado direito à variável do lado esquerdo. Além disso, existem operadores de atribuição compostos, como +=, -=, *=, /=, 
que combinam a operação aritmética com a atribuição.

Exemplos:
let x = 10; // Atribuição simples
x += 5; // Equivale a x = x + 5, agora x é 15
x -= 3; // Equivale a x = x - 3, agora x é 12
x *= 2; // Equivale a x = x * 2, agora x é 24
x /= 4; // Equivale a x = x / 4, agora x é 6
x %= 5; // Equivale a x = x % 5, agora x é 1

Esses operadores de atribuição permitem que você modifique o valor de uma variável de forma concisa e eficiente, 
evitando a necessidade de repetir a variável em expressões mais complexas.


*/