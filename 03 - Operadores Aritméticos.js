/*


Operadores Aritméticos -> São utilizados para realizar operações matemáticas entre os operandos.

+ -> Adição
- -> Subtração
* -> Multiplicação
/ -> Divisão
% -> Resto da divisão
** -> Exponenciação
++ -> Incremento
-- -> Decremento





*/

const result = 13 % 2; // Resto da divisão de 10 por 3

console.log(result); // 2

const result2 = ++13;

console.log(result2); // 14

const result3 = 2**3;

console.log(result3); // 8