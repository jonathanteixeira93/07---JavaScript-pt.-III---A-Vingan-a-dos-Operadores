/*


Operadores lógicos são usados para combinar expressões booleanas e retornar um valor booleano. Os principais operadores lógicos em JavaScript são:

1. E lógico (&&): Retorna true se ambas as expressões forem verdadeiras.
   Exemplo: true && true // true
2. OU lógico (||): Retorna true se pelo menos uma das expressões for verdadeira.
   Exemplo: true || false // true
3. NÃO lógico (!): Retorna o valor oposto da expressão.
   Exemplo: !true // false



*/

console.log(true && true); // true
console.log(true && false); // false
console.log(false && false); // false
console.log(true || false); // true
console.log(false || false); // false
console.log(!true); // false
console.log(!false); // true